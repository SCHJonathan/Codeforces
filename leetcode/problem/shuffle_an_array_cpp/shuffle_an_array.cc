const int RANGE = 1e9+7;

class Solution {
public:
    Solution(vector<int>& nums) {
        data = nums;
        origin = nums;
    }
    
    /** Resets the array to its original configuration and return it. */
    vector<int> reset() {
        data = origin;
        return origin;
    }
    
    /** Returns a random shuffling of the array. */
    vector<int> shuffle() {
        int n = data.size();
        for (int i = 0; i < n; i++) {
            int idx = rand()%(i+1);
            swap(data[idx], data[i]);
        }
        return data;
    }
private:
    vector<int> data;
    vector<int> origin;
};

